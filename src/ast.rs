#[derive(Debug, PartialEq)]
pub enum Token<'a> {
    Ident(&'a str),
    Int(i64),
    Assign,
    Plus,
    Minus,
    Slash,
    Star,
    Bang,
    Eq,
    NotEq,
    GtEq,
    LtEq,
    Comma,
    Semicolon,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LT,
    GT,
    Function,
    Let,
    True,
    False,
    If,
    Else,
    Return,
    Illegal,
}